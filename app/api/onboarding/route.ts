export async function GET(): Promise<any> {
  const clients = loadClients();
  return NextResponse.json({ clients, total: clients.length });
}

export async function POST(req: NextRequest): Promise<any> {
  try {
    const body = await req.json();
    const { action } = body;

    // ─── ONBOARD: full cascade trigger ───
    if (action === "onboard") {
      const form = body.form as Record<string, string>;
      if (!form.contactName || !form.email || !form.niche || !form.businessName) {
        return NextResponse.json({ error: "Required: contactName, email, niche, businessName" }, { status: 400 });
      }

      const results: Record<string, any> = {};
      const errors: string[] = [];

      // ── Step 1: Create GHL Contact ──
      try {
        const ghlPayload: any = {
          firstName: form.contactName.split(" ")[0],
          lastName: form.contactName.split(" ").slice(1).join(" ") || "",
          email: form.email,
          phone: form.phone || "",
          address1: form.city || "",
          source: "MC Onboarding Engine",
          tags: [form.niche, form.tier || "Starter", "mc-onboard"],
          locationId: GHL_LOC,
        };

        const ghlRes = await fetch(`${GHL_API}/contacts/`, {
          method: "POST",
          headers: { Authorization: `Bearer ${GHL_KEY}`, "Content-Type": "application/json", Version: "2021-07-28" },
          body: JSON.stringify(ghlPayload),
        });
        const ghlData = await ghlRes.json();
        if (ghlData.contact?.id) {
          results.ghlContactId = ghlData.contact.id;
          results.ghlContactName = ghlData.contact.contactName;

          // ── Step 2: Move GHL Contact to Onboarding Pipeline (Agreement stage) ──
          try {
            const oppRes = await fetch(`${GHL_API}/opportunities/`, {
              method: "POST",
              headers: { Authorization: `Bearer ${GHL_KEY}`, "Content-Type": "application/json", Version: "2021-07-28" },
              body: JSON.stringify({
                pipelineId: ONBOARDING_PIPELINE,
                pipelineStageId: ONBOARDING_STAGES.agreement,
                title: `${form.businessName} — ${form.tier || "Starter"}`,
                contactId: ghlData.contact.id,
                monetaryValue: form.tier === "Starter" ? 15000 : form.tier === "Growth" ? 25000 : 40000,
                status: "open",
                locationId: GHL_LOC,
              }),
            });
            const oppData = await oppRes.json();
            results.ghlOpportunityId = oppData.opportunity?.id;
            results.ghlPipelineStage = "Agreement";
          } catch (e: any) {
            errors.push(`GHL pipeline: ${e.message}`);
          }
        } else {
          errors.push(`GHL contact: ${JSON.stringify(ghlData).slice(0, 100)}`);
        }
      } catch (e: any) {
        errors.push(`GHL: ${e.message}`);
      }

      // ── Step 3: Create Fulfillment Record (direct write, no HTTP self-call) ──
      try {
        const record = createFulfillmentRecord({
          clientName: form.businessName,
          contactName: form.contactName,
          niche: form.niche,
          tier: form.tier || "Starter",
          city: form.city || "",
          email: form.email,
          phone: form.phone || "",
          startDate: new Date().toISOString(),
          ghlContactId: results.ghlContactId || "",
        });
        results.fulfillmentId = record.id;
      } catch (e: any) {
        errors.push(`Fulfillment: ${e.message}`);
      }

      // ── Step 4: Notify Jarvis with personalized brief ──
      const brief = buildBrief(form);
      try {
        const hookRes = await fetch(JARVIS_HOOK, {
          method: "POST",
          headers: { Authorization: `Bearer ${FLEET_TOKEN}`, "Content-Type": "application/json" },
          body: JSON.stringify({
            message: `FROM_ELON: New client onboarded via MC. Prepare welcome sequence.\n\n${brief}`,
          }),
          signal: AbortSignal.timeout(5000),
        });
        results.jarvisNotified = hookRes.ok;
      } catch {
        results.jarvisNotified = false;
        errors.push("Jarvis hook timeout (non-critical)");
      }

      // ── Step 5: Save record locally ──
      const record = {
        id: `onboard_${Date.now()}`,
        ...form,
        tier: form.tier || "Starter",
        createdAt: new Date().toISOString(),
        ghlContactId: results.ghlContactId,
        ghlOpportunityId: results.ghlOpportunityId,
        fulfillmentId: results.fulfillmentId,
        status: "agreement-sent",
        brief,
      };
      const clients = loadClients();
      clients.unshift(record);
      saveClients(clients);
      results.localRecordId = record.id;

      return NextResponse.json({
        success: true,
        results,
        errors,
        brief,
        clientName: form.businessName,
        message: `${form.businessName} onboarded! GHL contact ${results.ghlContactId ? "✓" : "✗"} · Pipeline ${results.ghlOpportunityId ? "✓" : "✗"} · Fulfillment ${results.fulfillmentId ? "✓" : "✗"} · Jarvis ${results.jarvisNotified ? "✓" : "✗"}`,
      });
    }

    return NextResponse.json({ error: "Unknown action" }, { status: 400 });
  } catch (err: any) {
    return NextResponse.json({ error: err.message }, { status: 500 });
  }
}
