import { NextRequest, NextResponse } from "next/server";

export const dynamic = "force-dynamic";

const GHL_API = "https://services.leadconnectorhq.com";
const GHL_KEY = process.env.GHL_API_KEY || "";
const GHL_LOC = process.env.GHL_LOCATION_ID || "";

// GHL Pipeline IDs
const ONBOARDING_PIPELINE = "R6G5m2bF6vxtoho9ZeQH";
const ONBOARDING_STAGES = {
  agreement: "b18bc20f-5f93-499e-a3a6-e89dc793f00c",
  paid: "3a073acd-3d6f-4d65-b41f-1f8a4c5cc040",
  onboarding_scheduled: "c56da4e2-28f4-4f24-82c2-e4b36c7e6e35",
  intake_form: "4d4c4dbd-7e64-4f0e-aabf-ec63b7d56d70",
  onboarded: "c06bd721-e4e2-4a12-9f08-7e3a7f1c2c3e",
};

const JARVIS_HOOK = "http://172.26.0.5:18790/hooks/agent";
const FLEET_TOKEN = "fleet_ops_2026";

const DATA_DIR = "/workspace/agents/elon";
const ONBOARDING_FILE = `${DATA_DIR}/onboarding-clients.json`;

import { existsSync, readFileSync, writeFileSync, mkdirSync } from "fs";
import { join } from "path";

// Fulfillment file - direct write (no self-HTTP)
const FULFILLMENT_FILE = join(process.cwd(), "data", "fulfillment.json");
function loadFulfillment(): any[] {
  try { if (existsSync(FULFILLMENT_FILE)) return JSON.parse(readFileSync(FULFILLMENT_FILE, "utf-8")); } catch {}
  return [];
}
function createFulfillmentRecord(data: any): any {
  const dir = join(process.cwd(), "data");
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
  const records = loadFulfillment();
  const record = { id: `f_${Date.now()}`, ...data, createdAt: new Date().toISOString(), stage: "onboarding", tasks: [] };
  records.unshift(record);
  writeFileSync(FULFILLMENT_FILE, JSON.stringify(records, null, 2));
  return record;
}

function loadClients(): any[] {
  try {
    if (existsSync(ONBOARDING_FILE)) return JSON.parse(readFileSync(ONBOARDING_FILE, "utf-8"));
  } catch {}
  return [];
}
function saveClients(data: any[]) {
  try {
    if (!existsSync(DATA_DIR)) mkdirSync(DATA_DIR, { recursive: true });
    writeFileSync(ONBOARDING_FILE, JSON.stringify(data, null, 2));
  } catch {}
}

